# 直感的ではない挙動
0.1 + 0.1 + 0.1 == 0.3
print( 0.1 + 0.1 + 0.1 == 0.3 )


# こちらは直感的な挙動
0.1 + 0.1 == 0.2
print( 0.1 + 0.1 == 0.2 )


# round()は第1引数の値を第2引数の桁数で丸める
round(0.1 + 0.1 + 0.1, 1) == round(0.3, 1)
print( round(0.1 + 0.1 + 0.1, 1) == round(0.3, 1) )


# 第1引数と第2引数が近ければTrueを返す
import math
math.isclose(0.1 + 0.1 + 0.1, 0.3)
print( math.isclose(0.1 + 0.1 + 0.1, 0.3) )
